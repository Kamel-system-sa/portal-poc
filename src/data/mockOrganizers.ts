const hajjCounts = [
  12500, 18750, 23400, 15600, 28900, // First 5
  32100, 14500, 26700, 19800, 35600, // Next 5
  41200, 17800, 29300, 22500, 38900, // Next 5
  45600, 16700, 27800, 31200, 42300  // Last 5
];
let hajjCountIndex = 0;

// Helper function to get next hajj count
const getRandomHajjCount = (): number => {
  return hajjCounts[hajjCountIndex++ % hajjCounts.length];
};

// Helper function to generate random phone number
const getRandomPhone = (): string => {
  return `05${Math.floor(Math.random() * 100000000).toString().padStart(8, '0')}`;
};

// Helper function to generate random email
const getRandomEmail = (company: string): string => {
  const domain = company.toLowerCase().replace(/\s+/g, '').replace(/[^a-z0-9]/g, '');
  return `contact@${domain}.com`;
};

